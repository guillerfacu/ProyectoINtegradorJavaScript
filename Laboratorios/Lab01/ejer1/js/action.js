

let fecha = new Date();
let diaS = fecha.getDay();

switch(diaS){

    case 0 : alert('River');
    break;
    case 1 : alert('Arranca la semana');
    break;
    case 2 : alert('Nada');
    break;
    case 3 : alert('Copa');
    break;
    case 4 : alert('Clase');
    break;
    case 5 : alert('Precia');
    break;
    case 6 : alert('Joda');
    break;
    default : alert('Error....');
    break;

}



